  final String imageUrl;
  final double salePrice;
  final double originalPrice;
  final String descriptionAr;
  final double rating;
  final bool isFeatured;
  final bool isActive;

  const MockProduct({
    required this.id,
    required this.titleAr,
    required this.titleEn,
    required this.categoryId,
    required this.imageUrl,
    required this.salePrice,
    required this.originalPrice,
    required this.descriptionAr,
    required this.rating,
    this.isFeatured = false,
    this.isActive = true,
  });

  double get price => salePrice;
  String get category => categoryId;

  Map<String, dynamic> toJson() => {
    'id': id,
    'titleAr': titleAr,
    'titleEn': titleEn,
    'categoryId': categoryId,
    'imageUrl': imageUrl,
    'salePrice': salePrice,
    'originalPrice': originalPrice,
    'descriptionAr': descriptionAr,
    'rating': rating,
    'isFeatured': isFeatured,
    'isActive': isActive,
  };

  factory MockProduct.fromJson(Map<String, dynamic> j) => MockProduct(
    id: j['id'] as String,
    titleAr: j['titleAr'] as String,
    titleEn: j['titleEn'] as String,
    categoryId: j['categoryId'] as String,
    imageUrl: j['imageUrl'] as String,
    salePrice: (j['salePrice'] as num).toDouble(),
    originalPrice: (j['originalPrice'] as num).toDouble(),
    descriptionAr: j['descriptionAr'] as String,
    rating: (j['rating'] as num).toDouble(),
    isFeatured: j['isFeatured'] as bool? ?? false,
    isActive: j['isActive'] as bool? ?? true,
  );
}

